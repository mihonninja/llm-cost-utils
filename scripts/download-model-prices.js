#!/usr/bin/env node

/**
 * Downloads the latest model‑pricing JSON from LiteLLM, merges
 * `src/data/model-prices-extra.json` (adds models / overrides fields),
 * and writes a TypeScript constant for use at runtime.
 */

import fs from 'fs'
import path from 'path'
import https from 'https'
import { fileURLToPath } from 'url'

const scriptDir = path.dirname(fileURLToPath(import.meta.url)) // "__dirname" equivalent

const MODEL_PRICES_URL = 'https://raw.githubusercontent.com/BerriAI/litellm/main/model_prices_and_context_window.json'
const OUTPUT_DIR = path.join(scriptDir, '../src/data')
const OUTPUT_FILE = path.join(OUTPUT_DIR, 'model-prices.ts')
const EXTRA_MODEL_PRICES_FILE = path.join(OUTPUT_DIR, 'model-prices-extra.json')

if (!fs.existsSync(OUTPUT_DIR)) {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true })
}

console.log(`Downloading model prices from ${MODEL_PRICES_URL}…`)

function download(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, (res) => {
        if (res.statusCode !== 200) {
          reject(new Error(`HTTP ${res.statusCode} ${res.statusMessage} while fetching ${url}`))
          res.resume()
          return
        }
        let data = ''
        res.on('data', (chunk) => (data += chunk))
        res.on('end', () => resolve(data))
      })
      .on('error', reject)
  })
}

function loadExtraModelPrices() {
  if (!fs.existsSync(EXTRA_MODEL_PRICES_FILE)) {
    console.warn(
      `No ${path.relative(path.join(scriptDir, '..'), EXTRA_MODEL_PRICES_FILE)} — using LiteLLM data only (create the file to add or override models).`,
    )
    return {}
  }
  const raw = fs.readFileSync(EXTRA_MODEL_PRICES_FILE, 'utf8')
  const parsed = JSON.parse(raw)
  if (parsed === null || typeof parsed !== 'object' || Array.isArray(parsed)) {
    throw new Error(`${EXTRA_MODEL_PRICES_FILE} must be a JSON object (model name → pricing fields).`)
  }
  return parsed
}

function mergeModelPrices(litellm, extra) {
  const merged = { ...litellm }
  for (const [name, patch] of Object.entries(extra)) {
    if (patch === null || typeof patch !== 'object' || Array.isArray(patch)) {
      throw new Error(`Extra entry "${name}" must be a JSON object.`)
    }
    const base = merged[name]
    merged[name] =
      base !== undefined && typeof base === 'object' && base !== null && !Array.isArray(base)
        ? { ...base, ...patch }
        : { ...patch }
  }
  return merged
}

;(async () => {
  try {
    const jsonRaw = await download(MODEL_PRICES_URL)
    const litellmPrices = JSON.parse(jsonRaw)
    const extraPrices = loadExtraModelPrices()
    const modelPrices = mergeModelPrices(litellmPrices, extraPrices)

    const extraCount = Object.keys(extraPrices).length
    if (extraCount > 0) {
      console.log(`Merged ${extraCount} model(s) from ${path.basename(EXTRA_MODEL_PRICES_FILE)}`)
    }

    const tsContent = `// Auto‑generated by scripts/download‑model‑prices.js\n// LiteLLM data merged with src/data/model-prices-extra.json — run npm run download-model-prices to refresh.\n\nexport const modelPricesData: Record<string, Record<string, unknown>> = ${JSON.stringify(
      modelPrices,
      null,
      2,
    )};\n`

    fs.writeFileSync(OUTPUT_FILE, tsContent, 'utf8')
    console.log(`Model prices saved to ${OUTPUT_FILE}`)

    // Sample output
    const examples = Object.keys(modelPrices).slice(0, 5)
    console.log('\nSample models:')
    examples.forEach((m) => {
      const p = modelPrices[m]
      console.log(`• ${m}: in $${p.input_cost_per_token * 1_000_000}/M, out $${p.output_cost_per_token * 1_000_000}/M`)
    })
    if (Object.keys(modelPrices).length > 5) {
      console.log('…and more')
    }
  } catch (err) {
    console.error('Failed:', err)
    process.exit(1)
  }
})()
